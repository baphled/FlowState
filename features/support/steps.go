// Side effects: May modify the test state.
func (s *StepDefinitions) allMessagesShouldBeRestored() error {
	if s.reloadedContextStore == nil {
		return errors.New("no reloaded context store available")
	}

	reloadedCount := s.reloadedContextStore.Count()
	if reloadedCount != s.originalMessageCount {
		return fmt.Errorf("expected %d messages, got %d", s.originalMessageCount, reloadedCount)
	}

	originalMsgs := s.contextStore.AllMessages()
	reloadedMsgs := s.reloadedContextStore.AllMessages()
	for i, orig := range originalMsgs {
		if reloadedMsgs[i].Role != orig.Role || reloadedMsgs[i].Content != orig.Content {
			return fmt.Errorf("message %d mismatch: expected %q/%q, got %q/%q",
				i, orig.Role, orig.Content, reloadedMsgs[i].Role, reloadedMsgs[i].Content)
		}
	}

	return nil
}

