// iShouldOnlySeeSessionsMentioning implements a BDD step definition.
//
// Expected: The step is executed successfully.
// Returns: nil on success, or an error if the step fails.
// Side effects: May modify the test state.
func (s *StepDefinitions) iShouldOnlySeeSessionsMentioning(text string) error {
	if len(s.sessions) == 0 {
		return errors.New("no sessions available")
	}
	if s.searchQuery == "" {
		return errors.New("no search query recorded")
	}
	if !strings.EqualFold(strings.TrimSpace(s.searchQuery), strings.TrimSpace(text)) {
		return fmt.Errorf("expected search query %q, got assertion for %q", s.searchQuery, text)
	}
	filtered := make(map[string]*TestSession)
	for name, session := range s.sessions {
		if sessionMentions(session, s.searchQuery) {
			filtered[name] = session
		}
	}
	if len(filtered) == 0 {
		return fmt.Errorf("expected at least one session mentioning %q", text)
	}
	for name, session := range filtered {
		if !sessionMentions(session, text) {
			return fmt.Errorf("session %q does not mention %q", name, text)
		}
	}
	s.sessions = filtered
	return nil
}

